string = "++++++++++[>+>+++>+++++++>++++++++++<<<<-]>>>++++++++++++++.------------.+++++.>+++++++++++++++++++++++.<<++++++++++++++++++.>>-------------------.---------.++++++++++++++.++++++++++++.<++++++++++++++++++.+++++++++.<+++.+.>----.>++++."



def decode(c):
    if c == '+': return "000"
    if c == '-': return "001"
    if c == '>': return "010"
    if c == '<': return "011"
    if c == '.': return "100"
    if c == ',': return "101"
    if c == '[': return "110"
    if c == ']': return "111"

bin_str = [decode(c) for c in string]

bin_str = "".join(bin_str)
print(bin_str)
